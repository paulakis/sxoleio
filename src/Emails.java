
public class Emails {
	String ar;
	String math;
	String diastma;
	int ap1;
	int ap2;
	String prosma;
	String prosgonea;
	String goneas;
	String mail;
	long kin;
	
	public Emails(String ar, String math, String diastma, int ap1, int ap2, String prosma, String prosgonea, String goneas,
			String mail, long kin) {
		super();
		this.ar = ar;
		this.math = math;
		this.diastma = diastma;
		this.ap1 = ap1;
		this.ap2 = ap2;
		this.prosma = prosma;
		this.prosgonea = prosgonea;
		this.goneas = goneas;
		this.mail = mail;
		this.kin = kin;
	}

	public Emails() {
		super();
	}

	public String getAr() {
		return ar;
	}

	public void setAr(String ar) {
		this.ar = ar;
	}

	public String getMath() {
		return math;
	}

	public void setMath(String math) {
		this.math = math;
	}

	public String getDiastma() {
		return diastma;
	}

	public void setDiastma(String diastma) {
		this.diastma = diastma;
	}

	public int getAp1() {
		return ap1;
	}

	public void setAp1(int ap1) {
		this.ap1 = ap1;
	}

	public int getAp2() {
		return ap2;
	}

	public void setAp2(int ap2) {
		this.ap2 = ap2;
	}

	public String getProsma() {
		return prosma;
	}

	public void setProsma(String prosma) {
		this.prosma = prosma;
	}

	public String getProsgonea() {
		return prosgonea;
	}

	public void setProsgonea(String prosgonea) {
		this.prosgonea = prosgonea;
	}

	public String getGoneas() {
		return goneas;
	}

	public void setGoneas(String goneas) {
		this.goneas = goneas;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public long getKin() {
		return kin;
	}

	public void setKin(long kin) {
		this.kin = kin;
	}
	
	

}
